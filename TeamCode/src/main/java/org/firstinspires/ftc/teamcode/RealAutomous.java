        public void cubecenter() {
        if (tfod != null) {
            // getUpdatedRecognitions() will return null if no new information is available since
            // the last time that call was made.
            List<Recognition> updatedRecognitions = tfod.getUpdatedRecognitions();
            float goldMineralX = 0;
            if (updatedRecognitions != null) {
                telemetry.addData("# Object Detected", updatedRecognitions.size());

                goldMineralX = -1;

                for (Recognition recognition : updatedRecognitions) {
                    if (recognition.getLabel().equals(LABEL_GOLD_MINERAL)) {
                        goldMineralX = ((recognition.getLeft() + recognition.getRight()) / 2);
                    }
                }
                telemetry.addData("GoldPos:", goldMineralX);
                if (goldMineralX == -1) {
                    smotor.setPower(0);
                } else if (goldMineralX < 400) {
                    smotor.setPower(-.25);
                } else if (goldMineralX > 560) {
                    smotor.setPower(.25);
                } else if (goldMineralX > 400 && goldMineralX < 560) {
                    if (goldMineralX < 520) {
                        smotor.setPower(-.15);
                    } else if (goldMineralX > 540) {
                        smotor.setPower(.15);
                    } else {
                        smotor.setPower(0);

                    }
                } else if (goldMineralX > 520 && goldMineralX < 540) {
                    smotor.setPower(0);

                }

            }
            telemetry.update();
        }
    }
